package ru.ElJORIKO.main.entrities;

public class FieldSize {
	private final int wight;
	private final int height;

	public FieldSize(int wight, int height) {
		this.wight = wight;
		this.height = height;
	}

	public int getHeight() {
		return height;
	}

	public int getWight() {
		return wight;
	}
}
